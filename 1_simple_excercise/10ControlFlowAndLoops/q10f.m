x = input("Enter first number:");
y = input("Enter second number:");
z = input("Enter third number:");

if x>y && x>z,
  disp("first number is greatest");
elseif y>x && y>z,
  disp("second number is greatest");
elseif z>x && z>y,
  disp("third number is greatest");
else,
  disp("Two or more possible answers");
endif
